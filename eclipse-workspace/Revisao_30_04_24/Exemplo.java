
public class Exemplo {

	public static void main(String[] args) {
		
		//Calculo de area e perimetro de um circulo
		// Area _circulo = r² * PI
		//PI = 3,14
		double areac;
		double raio= 6;
		double pi = 3.14;
		areac = (raio * raio)*pi;
		System.out.println("A area do seu circulo é igual a: "+areac);
		
		//Perimetro = 2*pi*raio
		double per; 
		per= (2*pi)*raio; 
		System.out.println("o valor do perimetro e igual a: "+ per);
		
		

	}

}
